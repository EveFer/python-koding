import math
import random

print(math.pow(10,2)) # eleva un numero a un exponente

print(math.sqrt(64)) # raiz cuadrada
print(math.isqrt(64)) # raiz cuadrada entero
print(math.sin(90))
print(math.cos(90))
print(math.tan(90))
print(math.factorial(3))

print(random.randint(1,100)) # devuelve un valor aleatorio entero