# Segmentado de cadenas
# substrings
cadena = "Esto es una cadena"
print(len(cadena)) # longitud de la cadena

print(cadena[2])

print(cadena[len(cadena) - 1]) # el ultimo caracter

cadena = "Esto es una cadena"
print(cadena[0:2])
print(cadena[:10]) # [0:10]
print(cadena[10:]) # [10:ultimo]
print(cadena[-3:])
