# Metodos de strings

cadena = 'metodos de Strings'
print(cadena.lower())
print(cadena.upper())
print(cadena.capitalize()) # la primer caracter en mayuscula
print(cadena.title()) # transforma en mayusculas el caracter inicial de cada palabra de un string
print(cadena.swapcase()) # las mayusculas a minuculas y viceversa