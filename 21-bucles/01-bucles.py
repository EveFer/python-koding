'''
while:

    while <condicion>:
        <Instrucciones del codigo>
        <Incrementar o decrementar el contador>

    n = 5
    while n > 0:
        print(n)
        n = n - 1
'''

'''
For:

for <Contador> in <Nombre de lista>:
    <Segemento de Código>

lista = [1,2,3,4,5]
for i in lista:
    print(i)
'''