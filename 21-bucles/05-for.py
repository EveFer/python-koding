# Recorrer elementos iterables ejemplo: listas|tuplas|strings|dictionario
# recorre rangos

lista = ['Uno', 'Dos', 'Tres']
tupla = (1,2,3,4)
string = 'Hola'
dicti = {'name': 'Fernanda', 'lastName': 'Palacios'}

for item in lista:
    print(item)

for item in tupla:
    print(item)

for item in string:
    print(item)

for item in dicti:
    print(item)

